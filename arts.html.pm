After nearly a 10-year hiatus, I started practicing drawing again after getting my PhD in fall
2022. I work mainly with fountain and dip pens. This is scientifically the best medium because (1)
it is binary: ink or not? (2) you cannot cheat (i.e. erase), and (3) fountain pens are the
greatest invention of any humanoid species.
